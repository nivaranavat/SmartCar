import neopixel
import board
import time

pixel_pin = board.D18
num_pixels = 8
ORDER = neopixel.GRB
pixels = neopixel.NeoPixel(pixel_pin, num_pixels, brightness = 0.2, auto_write= False, pixel_order = ORDER)

pixels.fill((160,160,160))
pixels.show()
time.sleep(5)






























































































































































































































































































































